package tp.greenComputing.Classes;

public class Sequ {

	
	
	
		
	private String app;
	private int frequence0;
	private String frequence1;
	private String frequence2;
	
	
		

	public Sequ(String app, int frequence0, String frequence1, String frequence2) {
		super();
		this.app = app;
		this.frequence0 = frequence0;
		this.frequence1 = frequence1;
		this.frequence2 = frequence2;
	}




	public void sequ() {}




	public String getApp() {
		return app;
	}




	public void setApp(String app) {
		this.app = app;
	}




	public int getFrequence0() {
		return frequence0;
	}




	public void setFrequence0(int frequence0) {
		this.frequence0 = frequence0;
	}




	public String getFrequence1() {
		return frequence1;
	}




	public void setFrequence1(String frequence1) {
		this.frequence1 = frequence1;
	}




	public String getFrequence2() {
		return frequence2;
	}




	public void setFrequence2(String frequence2) {
		this.frequence2 = frequence2;
	}
	
	
	
	
}
